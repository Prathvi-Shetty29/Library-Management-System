        // TODO add your handling code here:
        String url="jdbc:mysql://localhost:3306/library?useSSL=false";
        String mysqluser="root";
        String mysqlpwd="root@1290";
        String pswrd=new String(password.getPassword());
        String username=user.getText();
        String query=("select PASSWORD FROM admin where userid='"+username+"';");
        try{
            Connection conn = DriverManager.getConnection(url,mysqluser,mysqlpwd);
            Statement stm=conn.createStatement();
            ResultSet rs = stm.executeQuery(query);
            if(rs.next()){
                String realpswrd=rs.getString("password");
                if(realpswrd.equals(pswrd)){
                    DashBoard dsh=new DashBoard();
                    dsh.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this,"Enter valid username and Password");
                    
                }
                
                
            }else{
                 JOptionPane.showMessageDialog(this,"Enter valid username");
               
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
        
        
        
